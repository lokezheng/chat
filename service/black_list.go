// CheckValid checks if string contains any disable word
func CheckValid(text string) bool {
	r := []rune(text)
	isValid := true
	trie := patricia.NewTrie()
	for i, key := range BlackList {
		trie.Insert(patricia.Prefix(key), i)
	}
	for i := 0; i < len(r); i++ {
		if err := trie.VisitPrefixes(patricia.Prefix(string(r[i:])), checkFailed); err != nil {
			isValid = false
		}
	}

	return isValid
}

// WildcardReplace replaces forbidden words to wildcards
func WildcardReplace(text string) string {
	trie := patricia.NewTrie()
	for i, key := range BlackList {
		trie.Insert(patricia.Prefix(key), i)
	}

	return wildcardReplaceRec(trie, text)
}

func wildcardReplaceRec(trie *patricia.Trie, text string) string {
	r := []rune(text)
	for i := 0; i < len(r); i++ {
		for j := i + 1; j <= len(r); j++ {
			if err := trie.VisitPrefixes(patricia.Prefix(string(r[i:j])), checkFailed); err != nil {
				return wildcardReplaceRec(trie, wildcardReplace(i, j, text))
			}
		}
	}
	return text
}

func wildcardReplace(start, end int, text string) string {
	split := strings.Split(text, "")
	for i := start; i < end; i++ {
		split[i] = "*"
	}
	return strings.Join(split, "")
}

func checkFailed(prefix patricia.Prefix, item patricia.Item) error {
	return fmt.Errorf("%q: %v", prefix, item)
}

